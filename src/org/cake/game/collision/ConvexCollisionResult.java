package org.cake.game.collision;

import org.cake.game.geom.Vector2;

/**
 *
 * @author Aaron Cake
 */
public class ConvexCollisionResult {

    public Vector2 separatingAxis, collisionAxis;
    public float overlap;
    public boolean sourceA;
    public Object firstEvent;

    public ConvexCollisionResult(Vector2 separatingAxis, Vector2 collisionAxis, boolean sourceA, float overlap, Object firstEvent) {
        this.separatingAxis = separatingAxis;
        this.collisionAxis = collisionAxis;
        this.overlap = overlap;
        this.firstEvent = firstEvent;
        this.sourceA = sourceA;
    }

}
