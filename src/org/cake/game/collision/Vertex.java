package org.cake.game.collision;

import org.cake.game.geom.Vector2;

/**
 * A class keeping track of a point as well as the index of the point on a shape
 * @author Aaron Cake
 */
public class Vertex {

    public int index;
    public Vector2 point;

    public Vertex(int index, Vector2 point) {
        this.index = index;
        this.point = point;
    }

    public Vertex() {
        this.index = 0;
        this.point = null;
    }


}
