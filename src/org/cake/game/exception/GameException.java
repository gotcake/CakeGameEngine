package org.cake.game.exception;

/**
 * The base exception use by the game engine. Extends RuntimeException so catching is not needed.
 * @author Aaron Cake
 */
public class GameException extends RuntimeException {
    
    /**
     * Creates a GameException with the simple message "A GameException occurred."
     */
    public GameException() {
        super("A GameException occurred.");
    }
    
    /**
     * Creates a GameException with your own message
     */
    public GameException(String msg) {
        super(msg);
    }
    
    
    /**
     * Creates a GameException with your own message and cause
     */
    public GameException(String msg, Throwable cause) {
        super(msg, cause);
    }
    
    
    /**
     * Creates a GameException with your own cause
     */
    public GameException(Throwable cause) {
        super(cause);
    }
}
